const khash = {
    hash: function(data, len) {

        const decodedConfigKey = decode(process.env.CONFIG_KEY);

        const a = '0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ';
        const r = SHA1(`${data}.${decodedConfigKey}`);
        let c = data.toString().substr(0, 6);
        if (len > 17) {
            len = 17;
        }
        const limit = 2 * len;
        for (let i = 0; i < limit; i += 2) {
            c += a[parseInt(r.substr(i, 7), 16) % 36];
        }

        return c;
    }
}

